# 비교, 논리 연산자
x = 10
y = -10

# 비교 연산자
print(x > 0)
print(y > 0)
print()

print(x > y)
print(x < y)
print()

print(x == 10)  # true
print(x == y)   # false
print(x != y)   # true
print(x is y)   # false
print(x is not y)   # true
print()

# 논리 연산자
print(x > 0 and y > 0) # false
print(x > 0 or y > 0) # true
print(not y > 0) # true
